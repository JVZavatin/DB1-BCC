    private void addNew() throws SQLException, ClassNotFoundException, IOException{
       
        PaisDAO paisDAO = new PaisDAO();
        
        DiretorDAO diretorDAO = new DiretorDAO();
        
        ProdutoraDAO produtoraDAO = new ProdutoraDAO();
        
        Filme f= new Filme();
        FilmeDAO filmeDAO = new FilmeDAO();
        
        f.setNome(txtFilmeNome.getText());
        
        f.setAno(Integer.parseInt(txtFilmeAno.getText()));
       
        f.setProdutora(produtoraDAO.find((String)cbbProdutora.getSelectedItem()));
        f.setDiretor(diretorDAO.find((String)cbbDiretor.getSelectedItem()));
        
        filmeDAO.insert(f);
    }

