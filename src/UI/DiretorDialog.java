    private void addNew() throws SQLException, ClassNotFoundException, IOException{
       
        PaisDAO paisDAO = new PaisDAO();
        
        Diretor a= new Diretor();
        DiretorDAO diretorDAO = new DiretorDAO();
        
        a.setNome(txtDiretorNome.getText());
        
        Date data = null;
        String sData= txtDiretorData.getText();
        try {
        data = formatter.parse(sData);
               
        }catch (ParseException e) {    
        }
                
        a.setDataNasc(data);    
        a.setPais(paisDAO.find((String) cbbPais.getSelectedItem()));
        
        diretorDAO.insert(a);
    }

