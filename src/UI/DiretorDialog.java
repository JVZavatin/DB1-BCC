    private void btnSalvarDiretorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarDiretorActionPerformed
        //Date enteredDate = (Date)dateField.getValue(); pegandod valores de um formatted text field data com simple date format
        
         if (txtDiretorData.getText().length()<7){ // Evita que um valor '' seja inserido no banco
                        
                        JOptionPane.showMessageDialog(null,"Data invalida!");
                        
                    } else {
             
             
              if (txtDiretorNome.getText().length()<1){ 
                        
                        JOptionPane.showMessageDialog(null,"Nome vazio!");
              } else {        
                        
                        //enableButtons(false, true, true, true);

            int dialogResult = JOptionPane.showConfirmDialog(null, "Você tem certeza que deseja salvar esse registro?", "Confirmação?", JOptionPane.YES_NO_OPTION);


                if (dialogResult == JOptionPane.YES_OPTION) {
                    try {
                        if (addRecord == true) {
                            addNew();

                        } else {


                            updateRecord();
                        }
                        addRecord = false;

                        txtDiretorNome.setEnabled(false);
                        txtDiretorData.setEnabled(false);

                        txtDiretorNome.setBackground(Color.gray);
                        txtDiretorData.setBackground(Color.gray);

                        enableButtons(true, false, false, false);
                        clearInputBoxes();
                        enableFields(false);

                        loadRecords();

                    } catch (IOException | ClassNotFoundException | SQLException ex) {
                        System.out.println(ex.getMessage());
                   
                }
            }
        }
         }
    
    }//GEN-LAST:event_btnSalvarDiretorActionPerformed

 
    private void btnRemoverDiretorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverDiretorActionPerformed
        if (!txtDiretorData.getText().isEmpty()) {
            int dialogResult = JOptionPane.showConfirmDialog(null, "Você tem certeza que deseja excluir esse registro?", "Confirmação?", JOptionPane.YES_NO_OPTION);

            if (dialogResult == JOptionPane.YES_OPTION) {
                try {
                    deleteRecord();
                    loadRecords();
                    clearInputBoxes();
                    enableButtons(true, false, false, false);
                    
                    txtDiretorData.setEnabled(false);
                    txtDiretorData.setBackground(Color.gray);
                    
                    txtDiretorNome.setEnabled(false);
                    txtDiretorNome.setBackground(Color.gray);
                    
                    //cbbPais.setEnabled(false);
                    //cbbPais.setBackground(Color.gray);
                    
                } catch (SQLException ex) {
                    System.out.println(ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btnRemoverDiretorActionPerformed

