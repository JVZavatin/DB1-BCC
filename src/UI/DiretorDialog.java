    private void fillCBBPais() throws SQLException {
        PaisDAO dao = new PaisDAO();
        List<Pais> paises = dao.list();
        cbbPais.removeAllItems();
        for(Pais p : paises){
            cbbPais.addItem(p.getSigla());
        }        
    }
    
